''' 
Python函数执行时，需要处理变量时 会：
	1、首先查找函数内部是否存在指定名称的局部变量，如果有，直接使用
	2、如果没有，查找函数外部是否存在指定名称的全局变量，如果有，直接使用
	3、如果还没有找到，程序报错！

函数不能直接修改全局变量的引用

在函数内部修改全局变量的值需要 用到global进行声明

全局变量命名建议
	、全局变量名前应该增加 g_ 或者 gl_ 的前缀

'''


num = 10

def demo1():

	# global 关键字，告诉Python解释器 num 是一个全局变量
	global num
	num = 100  # 此时会修改全局变量num的值
	print(num)



